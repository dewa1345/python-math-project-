from fitur.sum import calculate_sum
from fitur.product import calculate_product
from fitur.div import div
from fitur.difference import calculate_difference

a = 10
b = 5
sum_result = calculate_sum(a, b)
print(sum_result)

sum_result = calculate_product(a, b)
print(sum_result)

sum_result = div(a, b)
print(sum_result)

sum_result = calculate_difference(a, b)
print(sum_result)



















